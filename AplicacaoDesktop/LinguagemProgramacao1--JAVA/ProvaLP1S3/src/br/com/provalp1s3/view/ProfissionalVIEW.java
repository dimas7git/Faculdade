        private void alterar(){
        try {
            profissionalDTO.setNome(this.inputNome.getText());
            profissionalDTO.setContato(this.inputContato.getText());
            profissionalDTO.setCursos(this.inputCursos.getText());
            profissionalDTO.setDescricao(this.inputDescricao.getText());
            profissionalDTO.setExperiencias(this.inputExperiencias.getText());
            profissionalDTO.setHabilidades(this.inputHabilidades.getText());
            profissionalDTO.setPreco(Float.parseFloat(this.inputPreco.getText()));
            
            JOptionPane.showMessageDialog(null, 
                    this.profissionalCTR.alterarProfissional(profissionalDTO));
        } catch(Exception e){
            System.out.println("Erro alterar()"+ e.getMessage());
        }
    }
    
    private void excluir(){
        if(JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o cliente?", "Aviso", 
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, this.profissionalCTR.deletarProfissional(profissionalDTO));
        }
    }
    
    private void preencherTabela(String nome){
        try {
            this.modeloJtlConsultarProfissional.setNumRows(0);
            
            profissionalDTO.setNome(nome);
            rs = profissionalCTR.consultarProfissional(profissionalDTO, 1);
            
            while(rs.next()){
                this.modeloJtlConsultarProfissional.addRow(new Object[] {
                    rs.getString("id_profissional"),
                    rs.getString("nome")
                });
            }
        } catch(Exception e){
            System.out.println("Erro preencherTabela(): " + e.getMessage());
        } finally{
            profissionalCTR.closeDb();
        }
    }
    
    private void preencherCampos(int id_profissional){
        try{
            this.profissionalDTO.setIdProfissional(id_profissional);
            rs = this.profissionalCTR.consultarProfissional(profissionalDTO, 2);
            if(rs.next()){
                this.limpaCampos();
                
                this.inputNome.setText(rs.getString("nome"));
                this.inputContato.setText(rs.getString("contato"));
                this.inputCursos.setText(rs.getString("cursos"));
                this.inputDescricao.setText(rs.getString("descricao"));
                this.inputExperiencias.setText(rs.getString("experiencias"));
                this.inputHabilidades.setText(rs.getString("habilidades"));
                this.inputPreco.setText(rs.getString("preco"));
                
                this.gravar_alterar = 2;
                this.liberaCampos(true);
            }
        } catch(Exception e){
            System.out.println("Erro: " + e.getMessage());
        } finally {
            this.profissionalCTR.closeDb();
        } 
    }
    
    private void gravar(){
        try {
            profissionalDTO.setNome(this.inputNome.getText());
            profissionalDTO.setContato(this.inputContato.getText());
            profissionalDTO.setCursos(this.inputCursos.getText());
            profissionalDTO.setDescricao(this.inputDescricao.getText());
            profissionalDTO.setExperiencias(this.inputExperiencias.getText());
            profissionalDTO.setHabilidades(this.inputHabilidades.getText());
            profissionalDTO.setPreco(Float.parseFloat(this.inputPreco.getText()));
            
            JOptionPane.showMessageDialog(null, profissionalCTR.inserirProfissional(profissionalDTO));
        } catch(Exception e){
            System.out.println("Erro gravar(): " + e.getMessage());
        }
    }

    public void setPosicao(){
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2,(d.height - this.getSize().height) / 2);
    }
    
    private void liberaCampos(boolean a){
        this.inputNome.setEnabled(a);
        this.inputContato.setEnabled(a);
        this.inputCursos.setEnabled(a);
        this.inputDescricao.setEnabled(a);
        this.inputExperiencias.setEnabled(a);
        this.inputHabilidades.setEnabled(a);
        this.inputPreco.setEnabled(a);
        
    }
    
    private void limpaCampos(){
        this.inputNome.setText("");
        this.inputContato.setText("");
        this.inputCursos.setText("");
        this.inputDescricao.setText("");
        this.inputExperiencias.setText("");
        this.inputHabilidades.setText("");
        this.inputPreco.setText("");
        
    }

    private void liberaBotoes(boolean a, boolean b, boolean c, boolean d, boolean e){
        this.btnNovo.setEnabled(a);
        this.btnCancelar.setEnabled(b);
        this.btnExcluir.setEnabled(d);
        this.btnSair.setEnabled(e);
        this.btnSalvar.setEnabled(c);
    }

